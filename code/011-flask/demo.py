# Flask 
# Flask is a Python web server we can use to host python / html / js / css / react (maybe..) 
# files for people to access
# Can be used as a backend that hosts files (backend + frontend)
# Can be a monolithic full stack, or just regular full stack 
# If a monolith crashes you lose everything (con), 
# typically easier and quicker to setup as the same language is being used to interact (pro) 

# Flask is installed via pip `pip install flask`